const BASE_URL =
  "https://v6.exchangerate-api.com/v6/53577c2c0cc6e7e38ae28a1f/latest/";

let selects = document.querySelectorAll(".dropdown");
let drp1 = document.querySelector(".dropdown1");
let img1 = document.querySelector(".img1");
let drp2 = document.querySelector(".dropdown2");
let img2 = document.querySelector(".img2");
let amount = document.querySelector(".amount");
let result = document.querySelector(".result");
let btn = document.querySelector(".btn");
let resultval = document.querySelector(".resultval");
for (let select of selects) {
  for (country in countryList) {
    let newoption = document.createElement("option");
    newoption.innerText = country;
    newoption.value = country;
    newoption.classList.add("option");
    select.append(newoption);
    if (select.name === "from" && newoption.value === "PKR") {
      newoption.selected = "selected";
    } else if (select.name === "to" && newoption.value === "USD") {
      newoption.selected = "selected";
    }
  }
}
const getex = async () => {
  const url = `${BASE_URL}/${drp1.value.toLowerCase()}`;
  let response = await fetch(url);
  let data = await response.json();
  let rate = data.conversion_rates[drp2.value];
  let total = rate * amount.value;
  resultval.innerText = `${amount.value} ${drp1.value}=${total} ${drp2.value}`;
};

drp1.addEventListener("change", () => {
  let cuncode = countryList[drp1.value];
  img1.innerHTML = `<img src="https://flagsapi.com/${cuncode}/flat/64.png"/>`;
  getex();
});
drp2.addEventListener("change", () => {
  let cuncode = countryList[drp2.value];
  img2.innerHTML = `<img src="https://flagsapi.com/${cuncode}/flat/64.png"/>`;
  getex();
});

getex();

btn.addEventListener("click", getex);
